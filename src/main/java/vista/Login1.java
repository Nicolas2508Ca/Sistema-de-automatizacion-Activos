    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String usuario = jTextField1.getText();
        String password = jPasswordField1.getText();

        if (usuario.isEmpty() || password.isEmpty()) 
        {
            JOptionPane.showMessageDialog(null, "Algun campo está vacío");
        } 
        else
        {
            try 
            {
                CConexion objetoConexion = new CConexion();
                Connection conexion = objetoConexion.establecerConexion();
                if (conexion != null) 
                {
                    String consulta = "SELECT * FROM Usuario WHERE Correo = ? AND Documento = ?";
                    try (PreparedStatement pst = conexion.prepareStatement(consulta)) 
                    {
                        pst.setString(1, usuario);
                        pst.setString(2, password);
                        try (ResultSet rs = pst.executeQuery()) 
                        {
                            if (rs.next()) 
                            {
                                int IdUsuario = rs.getInt("IdUsuario");
                                String Nombre = rs.getString("Nombre");
                                String Apellido = rs.getString("Apellido");
                                String Correo = rs.getString("Correo");
                                String Telefono = rs.getString("Telefono");
                                String TipoDocumento = rs.getString("TipoDocumento");
                                String Documento = rs.getString("Documento");
                                
                                Map<String, Object> usuarioLogeado = new HashMap<>();
                                usuarioLogeado.put("IdUsuario", IdUsuario);
                                usuarioLogeado.put("Nombre", Nombre);
                                usuarioLogeado.put("Apellido", Apellido);
                                usuarioLogeado.put("Correo", Correo);
                                usuarioLogeado.put("Telefono", Telefono);
                                usuarioLogeado.put("TipoDocumento", TipoDocumento);
                                usuarioLogeado.put("Documento", Documento);
                                
                                Perfil pr = new Perfil(usuarioLogeado);
                                pr.setVisible(true);
                                this.dispose();
                            } 
                            else 
                            {
                                JOptionPane.showMessageDialog(null, "Usuario o contraseña incorrectos");
                            }
                        }
                    }
                }
            } 
            catch (SQLException e) 
            {
                e.printStackTrace(); // Imprime detalles del error en la consola para depuración
                JOptionPane.showMessageDialog(null, "Error al establecer la conexión");
            }
        }    
    }//GEN-LAST:event_jButton1ActionPerformed
  
